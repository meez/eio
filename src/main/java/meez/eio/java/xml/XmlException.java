package meez.eio.java.xml;

/** XmlException */
public class XmlException extends RuntimeException {

  // Public methods

  /** Create new XmlException */
  public XmlException(String msg) {
    super(msg);
  }

  /** Create new XmlException */
  public XmlException(String msg, Throwable cause) {
    super(msg, cause);
  }
}